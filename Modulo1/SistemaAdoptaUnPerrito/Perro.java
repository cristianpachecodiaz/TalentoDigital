package SistemaAdoptaUnPerrito;

public class Perro {

	public String Id;
	public String Nombre;
	public String Raza;
	public String Edad;
	public String Vacunado;
	public String Operado;
		

	public Perro(String id, String nombre, String raza, String edad, String vacunado, String operado) {
	super();
	Id = id;
	Nombre = nombre;
	Raza = raza;
	Edad = edad;
	Vacunado = vacunado;
	Operado = operado;
	}
}